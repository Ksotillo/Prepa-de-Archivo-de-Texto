def suma(x, y):
    '''
    Restorna la suma entre los numeros x e y

    Argumentos>
    x -- primer numero a sumar
    y -- segundo numero a sumar

    Retorna>
    z -- la suma entre amos numeros
    '''
    z = x + y
    return z

def resta(x, y):
    '''
    Restorna la resta entre los numeros x e y

    Argumentos>
    x -- primer numero a restar
    y -- segundo numero a restar

    Retorna>
    z -- la resta entre amos numeros
    '''
    z = x - y
    return z

def multiplicar(x, y):
    '''
    Restorna la multiplicacion entre los numeros x e y

    Argumentos>
    x -- primer numero a multiplicar
    y -- segundo numero a multiplicar

    Retorna>
    z -- la multiplicacion entre amos numeros
    '''
    z = x * y
    return z

def dividir(x, y):
    '''
    Restorna la devision entre los numeros x e y

    Argumentos>
    x -- DIvidiendo
    y -- Divisor

    Retorna>
    z -- la devision entre amos numeros
    '''
